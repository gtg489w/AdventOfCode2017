function processRowDivisible(row) {
	row.sort(function(a, b){return a - b});
	var a, b;

	for(var i=0; i<row.length; i++) {
		for(var j = i+1; j<row.length; j++) {
			if(row[j] % row[i] == 0) {
				a = row[i];
				b = row[j];
			}
		}
	}
	return b/a;
}

