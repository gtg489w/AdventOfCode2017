function processRow(row) {
	var largest, smallest;
	row.forEach((num) => {
		if(!largest || num > largest) {
			largest = num;
		}
		if(!smallest || num < smallest) {
			smallest = num;
		}
	});
	return largest - smallest;
}

function part1() {
	var checksum = 0;
	input.forEach((row) => {
		checksum += processRow(row);
	});
	return checksum;
}

